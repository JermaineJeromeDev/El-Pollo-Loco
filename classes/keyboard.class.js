/**
 * Represents the keyboard status
 * @class Keyboard
 */
class Keyboard {
    /** Left arrow key */
    LEFT = false;
    
    /** Right arrow key */
    RIGHT = false;
    
    /** Up arrow key */
    UP = false;
    
    /** Down arrow key */
    DOWN = false;
    
    /** Spacebar (jump) */
    SPACE = false;
    
    /** D key (throw) */
    D = false;
    
    /** P key */
    P = false;
}